
class Operacoes:

	def __init__(self, x, y):
		self.x = x
		self.y = y

	def soma(self):
		return self.x + self.y

	def sub(self):
		return self.x - self.y

	def mult(self):
		return self.x * self.y

	def divisao(self):
		return self.x / self.y

	def potencia(self):
		return self.x ** self.y

	def divisaoInteira(self):
		return self.x // self.y
